    private void reset_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_reset_btnActionPerformed
        String answer = ans_txt.getText();
        String newpassword = newpwd_txt.getText();
        if(answer.equals(dbAnswer)){
            try{
            String query = "UPDATE `userbookstore` SET `upwd`=? WHERE `uemail`=?;";
            conn = DriverManager.getConnection(serverurl+timezone,username,password);
            ps = conn.prepareStatement(query);

            ps.setString(1, newpassword);
            ps.setString(2, email_txt.getText());
            if (ps.executeUpdate() >0)
                {
                    JOptionPane.showMessageDialog(null, "Update Data Successfully");
                    resetData_forgetpwd();
                }
            }
            catch(SQLException ex)
            {
                JOptionPane.showMessageDialog(this,"Cannot Update Data");
            }            
        }else{
            JOptionPane.showMessageDialog(null, "Incorrect Answer","Message", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_reset_btnActionPerformed

