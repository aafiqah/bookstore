    private void search_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_search_btnActionPerformed
        String email = email_txt.getText();
        userData user = null;
            
        try{
            String query = "SELECT * FROM `userbookstore` WHERE `uemail`=?;";
            conn = DriverManager.getConnection(serverurl+timezone,username,password);
            ps = conn.prepareStatement(query);

            ps.setString(1, email);
            rst = ps.executeQuery();
            
            if(rst.next())
            {
                user = new userData();
                user.setUsecurityques(rst.getString("usecurityques"));
                user.setUsecurityans(rst.getString("usecurityans"));
                
                if(user == null)
                {
                   JOptionPane.showMessageDialog(null, "Incorrect Email","Message", JOptionPane.ERROR_MESSAGE);
                }
                else{
                    search_btn.setEnabled(false);
                    email_txt.setEditable(false);
                    dbAnswer = user.getUsecurityans();
                    securityQuestion_combo.setSelectedItem(user.getUsecurityques());
                    validateFields_forget();
                }
            }
            else{
                 JOptionPane.showMessageDialog(null, "Incorrect Email","Message", JOptionPane.ERROR_MESSAGE);
                 search_btn.setEnabled(false);
            }
        }catch(HeadlessException | SQLException e){
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_search_btnActionPerformed

